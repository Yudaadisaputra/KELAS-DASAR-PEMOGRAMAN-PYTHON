# Variabel merupakan tempat penyimpanan data yang bersifat mutable, artinya nilainya bisa berubah-ubah.
# Variabel dalam python memiliki format penulisan nama_variabel = <nilai>.
# Variabel dapat berisi teks maupun bilangan.

# Berikut contoh Command atau Variable :

a = 1
print(a)
# a Merupakan Command atau Variable
# dan ditampilakn melalui Print dengan Variable a

a = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmn"
print(a)
# Tanda ("") Tersebut juga merupakan Command atau Variable
# dan ditampilakn melalui Print dengan Variable a

